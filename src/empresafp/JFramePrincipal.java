    void mostrarEmpresas() {
        String[] contenido = new String[empresas.size()];

        for (int i = 0; i < empresas.size(); i++) {
            contenido[i] = empresas.get(i).toString();
        }

        lstEmpresas.setListData(contenido);
    }

    boolean stringToFile(String[] cads, String nomeFicheiro) {
        boolean result = true;
        try {
            FileWriter ficheiro = new FileWriter(nomeFicheiro);
            PrintWriter pw = new PrintWriter(ficheiro);

            for (int i = 0; i < cads.length; i++) {
                pw.println(cads[i]);
            }

            ficheiro.close();

        } catch (IOException ex) {
            result = false;
        }
        return result;
    }

    void AbrirURL(String url) {
        if (Desktop.isDesktopSupported()) {
            Desktop desktop = Desktop.getDesktop();
            try {
                desktop.browse(new URI(url));
            } catch (URISyntaxException e) {
                System.err.println("Error: Sintaxis de URL incorrecta.\n" + e.getMessage());
            } catch (IOException e) {
                System.err.println("Error: No se pudo abrir el enlace.\n" + e.getMessage());
            }
        } else {
            System.err.println("Error: Error de compatibilidad en la plataforma actual. No se puede abrir enlaces web.");
        }
    }

